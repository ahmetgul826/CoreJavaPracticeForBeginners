package com.myproject.Day02Ex;

public class AssignmentOp {
    public static void main(String[] args) {
        int a = 10;//a = 10
        int b = 5;
        b += 3; //b = b + 3;

        int c = 10;
        c -= 4; //c = c - 4;

        int e = 20;
        e /= 4; //e = e / 4;

        int x = 15;
        x %= 6; //x = x % 6;


        System.out.println(a + "\n" + b + "\n" + c + "\n" + e + "\n" + x);



    }
}
